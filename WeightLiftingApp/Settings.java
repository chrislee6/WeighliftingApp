
public class Settings
{
    public int maxFS;
    public int maxHC;
    public int maxDL;
    
    public Settings()
    {
    }

    public void setMaxFS(int maxFS)
    {
        this.maxFS = maxFS;
    }
    public int getMaxFS()
    {
        return maxFS;
    }
    
    public void setMaxHC(int maxHC)
    {
        this.maxHC = maxHC;
    }
    public int getMaxHC()
    {
        return maxHC;
    }
    
    public void setMaxDL(int maxDL)
    {
        this.maxDL = maxDL;
    }
    public int getMaxDL()
    {
        return maxDL;
    }

}
